public  class Contact  {
    public String   name;
    public String number;


    public Contact(String name, String  number) {
        this.name = name;
        this.number = number;
    }

    public String getName() {
        return name;
    }


    public String getNumber() {
        return number;
    }

    public void setName() {
        this.name = name;
    }

    public void setNumber() {
        this.number = number;
    }
}
